package br.com.primeiroprogramakotlin

fun main() {

    var num1 :Int = 5
    var num2 :Int = 10

    println(num1 + num2)
    println(num1.plus(num2))
    println("==============")
    println(num1 - num2)
    println(num1.minus(num2))
    println("==============")
    println(num1 * num2)
    println(num1.times(num2))
    println("==============")
    println(num1 / num2)
    println(num1.div(num2))
    println("==============")
    println(num1 % num2)
    println(num1.mod(num2))
    println("==============")
}